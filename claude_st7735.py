def _fi(c):
    o=ord(c)
    if o==32: return 0
    if 48<=o<=57: return 1+(o-48)
    if 65<=o<=90: return 11+(o-65)
    if 97<=o<=122: return 37+(o-97)
    return 0

class ST7735:
    def __init__(self, spi, dc, cs, rst, w=128, h=160, rot=0):
        self.spi=spi; self.dc=dc; self.cs=cs; self.rst=rst
        self.w=w; self.h=h; self.rot=0
        self.reset(); self._init(); self.set_rot(rot)

    def reset(self):
        self.rst.value(1); sleep_ms(50)
        self.rst.value(0); sleep_ms(50)
        self.rst.value(1); sleep_ms(150)

    def _cmd(self, c):
        self.dc.value(0); self.cs.value(0)
        self.spi.write(bytes([c])); self.cs.value(1)

    def _dat(self, d):
        self.dc.value(1); self.cs.value(0)
        self.spi.write(bytes([d]) if isinstance(d,int) else d)
        self.cs.value(1)

    def _init(self):
        self._cmd(1); sleep_ms(150)
        self._cmd(0x11); sleep_ms(255)
        self._cmd(0xB1); self._dat(b'\x01\x2c\x2d')
        self._cmd(0xB2); self._dat(b'\x01\x2c\x2d')
        self._cmd(0xB3); self._dat(b'\x01\x2c\x2d\x01\x2c\x2d')
        self._cmd(0xB4); self._dat(7)
        self._cmd(0xC0); self._dat(b'\xa2\x02\x84')
        self._cmd(0xC1); self._dat(0xC5)
        self._cmd(0xC2); self._dat(b'\x0a\x00')
        self._cmd(0xC3); self._dat(b'\x8a\x2a')
        self._cmd(0xC4); self._dat(b'\x8a\xee')
        self._cmd(0xC5); self._dat(0x0E)
        self._cmd(0x20)
        self._cmd(_MA); self._dat(0xC8)
        self._cmd(_CM); self._dat(5)
        self._cmd(0xE0)
        self._dat(b'\x02\x1c\x07\x12\x37\x32\x29\x2d'
                  b'\x29\x25\x2b\x39\x00\x01\x03\x10')
        self._cmd(0xE1)
        self._dat(b'\x03\x1d\x07\x06\x2e\x2c\x29\x2d'
                  b'\x2e\x2e\x37\x3f\x00\x00\x02\x10')
        self._cmd(0x13); sleep_ms(10)
        self._cmd(0x29); sleep_ms(100)

    def set_rot(self, r):
        self.rot=r&3; self._cmd(_MA)
        if self.rot==0: self._dat(0xC8); self.w,self.h=128,160
        elif self.rot==1: self._dat(0x68); self.w,self.h=160,128
        elif self.rot==2: self._dat(8); self.w,self.h=128,160
        else: self._dat(0xA8); self.w,self.h=160,128

    def _win(self,x0,y0,x1,y1):
        self._cmd(_CA); self._dat(bytes([0,max(0,min(255,x0)),0,max(0,min(255,x1))]))
        self._cmd(_RA); self._dat(bytes([0,max(0,min(255,y0)),0,max(0,min(255,y1))]))
        self._cmd(_RW)

    def pixel(self,x,y,c):
        if 0<=x<self.w and 0<=y<self.h:
            self._win(x,y,x,y); self._dat(bytes([c>>8,c&0xFF]))

    def fill(self,c):
        self.rect(0,0,self.w,self.h,c)

    def rect(self,x,y,w,h,c):
        if x>=self.w or y>=self.h or w<=0 or h<=0: return
        if x+w>self.w: w=self.w-x
        if y+h>self.h: h=self.h-y
        self._win(x,y,x+w-1,y+h-1)
        cb=bytes([c>>8,c&0xFF]); ch=cb*32
        p=w*h; self.dc.value(1); self.cs.value(0)
        while p>=32: self.spi.write(ch); p-=32
        if p>0: self.spi.write(cb*p)
        self.cs.value(1)

    def hline(self,x,y,w,c): self.rect(x,y,w,1,c)
    def vline(self,x,y,h,c): self.rect(x,y,1,h,c)

    def line(self,x0,y0,x1,y1,c):
        s=abs(y1-y0)>abs(x1-x0)
        if s: x0,y0,x1,y1=y0,x0,y1,x1
        if x0>x1: x0,x1,y0,y1=x1,x0,y1,y0
        dx=x1-x0; dy=abs(y1-y0); err=dx//2; ys=-1 if y0>y1 else 1
        while x0<=x1:
            self.pixel(y0 if s else x0, x0 if s else y0, c)
            err-=dy
            if err<0: y0+=ys; err+=dx
            x0+=1

    def circle(self,x0,y0,r,c):
        x,y,e=r,0,0
        while x>=y:
            self.pixel(x0+x,y0+y,c); self.pixel(x0+y,y0+x,c)
            self.pixel(x0-y,y0+x,c); self.pixel(x0-x,y0+y,c)
            self.pixel(x0-x,y0-y,c); self.pixel(x0-y,y0-x,c)
            self.pixel(x0+y,y0-x,c); self.pixel(x0+x,y0-y,c)
            y+=1; e+=1+2*y
            if 2*(e-x)+1>0: x-=1; e+=1-2*x

    def fill_circle(self,x0,y0,r,c):
        x,y,e=r,0,0
        while x>=y:
            self.vline(x0-x,y0+y,2*x+1,c); self.vline(x0+x,y0+y,2*x+1,c)
            self.vline(x0-y,y0+x,2*y+1,c); self.vline(x0+y,y0+x,2*y+1,c)
            self.vline(x0-y,y0-x,2*y+1,c); self.vline(x0+y,y0-x,2*y+1,c)
            self.vline(x0-x,y0-y,2*x+1,c); self.vline(x0+x,y0-y,2*x+1,c)
            y+=1; e+=1+2*y
            if 2*(e-x)+1>0: x-=1; e+=1-2*x

    def text(self,s,x,y,c,bg=None,sz=1):
        for ch in s:
            self._ch(x,y,ch,c,bg,sz); x+=8*sz

    def _ch(self,x,y,ch,c,bg,sz):
        i=_fi(ch)*8; b=_F[i:i+8]
        for j in range(8):
            r=b[j]
            for i in range(8):
                if r&(1<<7-i):
                    if sz==1: self.pixel(x+i,y+j,c)
                    else: self.rect(x+i*sz,y+j*sz,sz,sz,c)
                elif bg is not None:
                    if sz==1: self.pixel(x+i,y+j,bg)
                    else: self.rect(x+i*sz,y+j*sz,sz,sz,bg)

    def tw(self,s,sz=1): return len(s)*8*sz
    def center(self,s,y,c,bg=None,sz=1):
        self.text(s,(self.w-self.tw(s,sz))//2,y,c,bg,sz)
